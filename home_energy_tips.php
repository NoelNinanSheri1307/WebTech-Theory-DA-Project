            <li><strong>Switch to LED Bulbs:</strong> Replace traditional incandescent bulbs with LED lights, which use less energy and last longer.</li>
            <li><strong>Unplug Devices When Not in Use:</strong> Many devices use standby power; unplug them when they’re not needed to save energy.</li>
            <li><strong>Use Smart Power Strips:</strong> Smart strips automatically cut off power to devices that are in standby mode, reducing "phantom" energy consumption.</li>
            <li><strong>Optimize Thermostat Settings:</strong> Set your thermostat a few degrees lower in winter and higher in summer to reduce heating and cooling costs.</li>
            <li><strong>Insulate Your Home:</strong> Improve insulation in your walls, attic, and windows to maintain temperature and reduce energy loss.</li>
            <li><strong>Use Energy-Efficient Appliances:</strong> When purchasing new appliances, look for Energy Star-rated models that use less electricity and water.</li>
            <li><strong>Wash Clothes with Cold Water:</strong> Washing with cold water can reduce energy usage significantly, as heating water is one of the biggest energy consumers in homes.</li>
            <li><strong>Dry Clothes Naturally:</strong> Use a clothesline or drying rack instead of a dryer when possible to save electricity.</li>
            <li><strong>Limit Hot Water Usage:</strong> Set your water heater to 120°F (49°C) and install low-flow showerheads to reduce hot water consumption.</li>
            <li><strong>Seal Air Leaks:</strong> Check windows, doors, and ducts for drafts, and seal any leaks to keep conditioned air inside your home.</li>
